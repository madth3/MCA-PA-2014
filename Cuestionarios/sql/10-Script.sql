CREATE TABLE CUESTIONARIO(
  ID INTEGER PRIMARY KEY GENERATED ALWAYS AS IDENTITY(START WITH 1, INCREMENT BY 1),
  NOMBRE VARCHAR(100) NOT NULL,
  NUMERO_REACTIVOS INTEGER,
  FECHA_CREACION DATE,
  TIPO_CUESTIONARIO VARCHAR(10),
  RATING DECIMAL(3,2)
);

